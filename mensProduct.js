// import appendProd from "./kidsScript/append.js";

function appendProd(data, container) {
  let cartArr = JSON.parse(localStorage.getItem("cartProd")) || [];
  container.innerHTML = null;
  data.forEach((ele) => {
    let box = document.createElement("div");

    let title = document.createElement("p");
    title.innerText = ele.title;
    title.id = "prodTitle";

    let image = document.createElement("img");
    image.src = ele.imgUrl;
    image.id = "img";
    image.addEventListener("mouseover", changeimg);
    image.addEventListener("mouseout", returnimg);

    let hovimg = document.createElement("img");
    hovimg.src = ele.hovimg;
    hovimg.id = "hovimg";
    let price = document.createElement("p");
    price.innerText = `â‚¬ ${ele.price}`;
    price.id = "prodPrice";

    let btn = document.createElement("button");
    btn.innerText = "Add to Cart";
    btn.id = "cartBtn";
    btn.addEventListener("click", function () {
      cartArr.push(ele);
      localStorage.setItem("cartProd", JSON.stringify(cartArr));
    });

    box.append(image, title, price, btn);
    container.append(box);

    function changeimg() {
      image.setAttribute("src", ele.hovimg);
    }

    function returnimg() {
      image.setAttribute("src", ele.imgUrl);
    }
  });
}

let container = document.querySelector("#products");

appendProd(mensData, container);

document.querySelector("#length").innerHTML = `${mensData.length} Products`;

// import { PriceSort } from "./kidsScript/sorting.js";
//sorting
function PriceSort(appendProd, data, container) {
  let selected = document.querySelector("#sort").value;
  if (selected === "Acs") {
    data.sort(function (a, b) {
      return parseFloat(a.price) - parseFloat(b.price);
    });
    appendProd(data, container);
  }
  if (selected === "Desc") {
    data.sort(function (a, b) {
      return parseFloat(b.price) - parseFloat(a.price);
    });
    appendProd(data, container);
  }
}

document.querySelector("#sort").addEventListener("change", function () {
  PriceSort(appendProd, mensData, container);
});


